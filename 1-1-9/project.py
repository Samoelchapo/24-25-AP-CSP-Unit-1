import turtle as trtl
#Semi Circle size
painter = trtl.Turtle()
painter.goto(0,0)
painter.speed(0)
painter.pensize(5)
painter.fillcolor('black')
painter.circle(180,180)
painter.right(220)
painter.penup()
painter.goto(-230,80)
painter.pendown()
painter.circle(180,180)
painter.penup()























wn = trtl.Screen()
wn.mainloop()