import turtle as trtl


#Small crescent size
painter = trtl.Turtle()
painter.penup()
painter.goto(-350,350)
painter.pendown()
painter.speed(2)
painter.pensize(5)
painter.fillcolor('white')
painter.right(180)
painter.circle(70,180)
#Big crescent



painter.right(-90)
painter.circle(60,180)
painter.penup()























wn = trtl.Screen()
wn.mainloop()